task usercontrol(){
	int leftEncoder =0, rightEncoder=0, armLeftEncoder=0, armRightEncoder=0;

	while(true){
		motor[rightFront] = vexRT[Ch2];
		motor[rightBack] = vexRT[Ch2];
		motor[leftFront] = vexRT[Ch3];
		motor[leftBack] = vexRT[Ch3];

		//Arm Control
		if(vexRT[Btn6UXmtr2]){
			if(SensorValue[armSolenoid] == 1)
				SensorValue[armSolenoid] = 0;
			motor[armLeft1] = 127;
			motor[armLeft2] = 127;
			motor[armRight1] = 127;
			motor[armRight2] = 127;
		}else if(vexRT[Btn6DXmtr2]){
			motor[armLeft1] = -127;
			motor[armLeft2] = -127;
			motor[armRight1] = -127;
			motor[armRight2] = -127;
		}else{
			motor[armLeft1] = 0;
			motor[armLeft2] = 0;
			motor[armRight1] = 0;
			motor[armRight2] = 0;
		}
		if(vexRT[Btn5UXmtr2]){
			motor[intakeLeft] = 127;
			motor[intakeRight] = 127;
		}else if(vexRT[Btn5DXmtr2]){
			motor[intakeLeft] = -127;
			motor[intakeRight] = -127;
		}else{
			motor[intakeLeft] = 0;
			motor[intakeRight] = 0;
		}
		if(vexRT[Btn7RXmtr2]){
			SensorValue[armSolenoid] = 1;
		}
		if(vexRT[Btn7DXmtr2]){
			SensorValue[claw] = 0;
		}else{
			SensorValue[claw] = 1;
		}
	}
}
